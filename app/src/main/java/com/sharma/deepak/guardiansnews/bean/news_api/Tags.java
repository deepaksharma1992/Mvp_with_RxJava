public class Tags {
    private String id, type, webTitle, webUrl, apiUrl, bio, bylineImageUrl, firstName, lastName;

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getWebTitle() {
        return webTitle;
    }

    public String getWebUrl() {
        return webUrl;
    }

    public String getApiUrl() {
        return apiUrl;
    }

    public String getBio() {
        return bio;
    }

    public String getBylineImageUrl() {
        return bylineImageUrl;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }
}

